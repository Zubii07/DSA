/*Linked Lists are a type of data structure used for storing collections of data. The data is stored 
  in nodes, each of which contains a data field and a reference (link) to the next node in the sequence.
  Linked Lists are used in other types of data structures like stacks and queues.


  Types: 
     Two types of linked lists are commonly used: 
       Singly linked lists, where each node points to the next node and the last node points to null. 
       Doubly linked lists, where each node has two links, one to the previous node and another one to the next. 


 Linked Lists vs Arrays (Comparison)
        Linked lists consist of nodes, and is a linear data structure we make ourselves,
        unlike arrays which is an existing data structure in the programming language that we can use.
   */